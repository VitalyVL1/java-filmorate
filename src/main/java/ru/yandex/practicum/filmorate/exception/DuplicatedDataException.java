package ru.yandex.practicum.filmorate.exception;

/**
 * Исключение, выбрасываемое при попытке создать дублирующиеся данные.
 * <p>
 * Возникает, когда нарушается уникальность данных, например:
 * <ul>
 *   <li>попытка зарегистрировать пользователя с email, который уже существует</li>
 *   <li>попытка создать фильм с неуникальным идентификатором</li>
 *   <li>повторное добавление лайка от одного пользователя к одному фильму</li>
 *   <li>попытка создать уже существующую дружескую связь</li>
 * </ul>
 * </p>
 *
 * <p>Наследуется от {@link RuntimeException}, что позволяет не объявлять исключение
 * в сигнатуре метода. При обработке в {@code ExceptionHandler} преобразуется
 * в HTTP-ответ со статусом 400 Bad Request.</p>
 */
public class DuplicatedDataException extends RuntimeException {

    /**
     * Создает новое исключение с указанным сообщением.
     *
     * @param message детальное сообщение о том, какие данные дублируются
     */
    public DuplicatedDataException(String message) {
        super(message);
    }
}