package ru.yandex.practicum.filmorate.exception;

/**
 * Исключение, выбрасываемое при внутренних ошибках сервера.
 * <p>
 * Используется для обозначения критических ошибок, возникших на стороне сервера
 * и не связанных с некорректными действиями пользователя. Обычно свидетельствует
 * о проблемах с подключением к базе данных, ошибках в SQL-запросах,
 * недоступности внешних сервисов или других технических неполадках.
 * </p>
 *
 * <p>Наследуется от {@link RuntimeException}, что позволяет не объявлять исключение
 * в сигнатуре метода. При обработке в {@code ExceptionHandler} преобразуется
 * в HTTP-ответ со статусом 500 Internal Server Error.</p>
 *
 * <p>Примеры ситуаций:
 * <ul>
 *   <li>Ошибка подключения к базе данных</li>
 *   <li>Неверный синтаксис SQL-запроса</li>
 *   <li>Проблемы с файловой системой при работе с in-memory хранилищем</li>
 *   <li>Неожиданные ошибки во время выполнения операций с данными</li>
 * </ul>
 * </p>
 */
public class InternalServerException extends RuntimeException {

    /**
     * Создает новое исключение с указанным сообщением.
     *
     * @param message детальное сообщение о возникшей внутренней ошибке
     */
    public InternalServerException(String message) {
        super(message);
    }
}