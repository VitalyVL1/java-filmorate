package ru.yandex.practicum.filmorate.exception;

/**
 * Исключение, выбрасываемое при попытке обращения к несуществующему ресурсу.
 * <p>
 * Возникает, когда запрашиваемый объект (фильм, пользователь, жанр, рейтинг MPA
 * или дружеская связь) не найден в системе. Это наиболее часто используемое
 * исключение в приложении, так как большинство операций требуют проверки
 * существования объектов.
 * </p>
 *
 * <p>Примеры ситуаций:
 * <ul>
 *   <li>Запрос фильма по несуществующему ID</li>
 *   <li>Попытка обновить несуществующего пользователя</li>
 *   <li>Добавление лайка от несуществующего пользователя</li>
 *   <li>Поиск друзей у несуществующего пользователя</li>
 *   <li>Запрос жанра или рейтинга MPA с недопустимым ID</li>
 * </ul>
 * </p>
 *
 * <p>Наследуется от {@link RuntimeException}, что позволяет не объявлять исключение
 * в сигнатуре метода. При обработке в {@code ExceptionHandler} преобразуется
 * в HTTP-ответ со статусом 404 Not Found.</p>
 *
 * @see ru.yandex.practicum.filmorate.exception_handler.controller.ErrorHandlingController
 */
public class NotFoundException extends RuntimeException {

    /**
     * Создает новое исключение с указанным сообщением.
     *
     * @param message детальное сообщение о том, какой ресурс не найден
     *                (обычно содержит тип объекта и его идентификатор)
     */
    public NotFoundException(String message) {
        super(message);
    }
}