package ru.yandex.practicum.filmorate.model;

/**
 * Перечисление возможных статусов дружеских связей между пользователями.
 * <p>
 * Определяет текущее состояние отношений между двумя пользователями
 * в системе дружбы. Статус может изменяться в процессе взаимодействия
 * пользователей.
 * </p>
 *
 * <p>Возможные статусы:
 * <ul>
 *   <li>{@link #UNCONFIRMED} - неподтвержденная дружба (запрос отправлен,
 *       но еще не принят второй стороной)</li>
 *   <li>{@link #CONFIRMED} - подтвержденная дружба (оба пользователя
 *       подтвердили дружескую связь)</li>
 * </ul>
 * </p>
 *
 * <p>При создании дружеской связи по умолчанию устанавливается статус
 * {@link #UNCONFIRMED}. Статус меняется на {@link #CONFIRMED} после того,
 * как второй пользователь подтверждает дружбу.</p>
 *
 * @see ru.yandex.practicum.filmorate.model.User
 */
public enum FriendStatus {

    /**
     * Подтвержденная дружба.
     * <p>
     * Статус означает, что оба пользователя подтвердили дружескую связь.
     * В этом состоянии связь считается двусторонней и отображается
     * в списках друзей обоих пользователей.
     * </p>
     */
    CONFIRMED,

    /**
     * Неподтвержденная дружба (запрос на дружбу).
     * <p>
     * Начальный статус при создании дружеской связи. Означает, что один
     * пользователь отправил запрос на добавление в друзья, но второй
     * пользователь еще не ответил на запрос. В этом состоянии связь
     * является односторонней.
     * </p>
     */
    UNCONFIRMED
}